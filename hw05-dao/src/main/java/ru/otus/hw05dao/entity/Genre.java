package ru.otus.hw05dao.entity;

import ru.otus.hw05dao.builder.GenreBuilder;

import java.util.Objects;

public class Genre {

    private long id;

    private final String name;

    public Genre(String name){
        this.name = name;
    }

    public Genre(long id, String name){
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public GenreBuilder toBuilder(){
        return new GenreBuilder(id, name);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        Genre genre = (Genre) o;
        return id == genre.id;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }

    @Override
    public String toString() {
        return "Genre{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
